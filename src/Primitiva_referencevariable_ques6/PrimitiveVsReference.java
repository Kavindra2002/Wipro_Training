package Primitiva_referencevariable_ques6;

//Primitive Variables: Store actual values (e.g., int, char, boolean).
//Reference Variables: Store references (memory addresses) to objects.

public class PrimitiveVsReference {
    public static void main(String[] args) {
        // Primitive variable
        int primitiveVar = 10;
        System.out.println("Primitive Variable: " + primitiveVar);

        // Reference variable
        String referenceVar = new String("Hello");
        System.out.println("Reference Variable: " + referenceVar);
    }
}
